document.addEventListener("DOMContentLoaded", () => {
    // Skill Cards Animation
    const skillCards = document.querySelectorAll(".skill-card");
    const skillObserver = new IntersectionObserver((entries) => {
      entries.forEach((entry) => {
        if (entry.isIntersecting) {
          entry.target.style.opacity = "1";
          entry.target.style.transform = "translateY(0)";
        }
      });
    });
  
    skillCards.forEach((card) => {
      skillObserver.observe(card);
    });
  
    // Education & Certificates Scroll Animation
    const cards = document.querySelectorAll('.education-card, .certificate-card');
    const observerOptions = {
      threshold: 0.5
    };
  
    const observer = new IntersectionObserver((entries, observer) => {
      entries.forEach(entry => {
        if (entry.isIntersecting) {
          entry.target.classList.add('is-visible');
          observer.unobserve(entry.target);
        }
      });
    }, observerOptions);
  
    cards.forEach(card => {
      observer.observe(card);
    });
  
    // Interests Scroll Animation
    const interestCards = document.querySelectorAll('.interest-card');
    const interestObserver = new IntersectionObserver((entries, observer) => {
      entries.forEach(entry => {
        if (entry.isIntersecting) {
          entry.target.classList.add('is-visible');
          observer.unobserve(entry.target);
        }
      });
    }, observerOptions);
  
    interestCards.forEach(card => {
      interestObserver.observe(card);
    });
  });
  